// array
const array = [1, 2, 3];

document.writeln(`array = [1,2,3] and array[0]=${array[0]}`);
document.writeln('<br>');

// multi-dimensional arrays
const matrix = [
  [1, 2, 3],
  [4, 5, 6],
  [7, 8, 9],
];

document.writeln(`matrix[0][0]=${matrix[0][0]}`);
document.writeln('<br>');
document.writeln(`matrix[2][0]=${matrix[2][0]}`);
