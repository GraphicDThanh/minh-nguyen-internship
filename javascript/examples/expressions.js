// Arithmetic expressions
const a = 5;
const b = 3;
document.writeln(a || b);

// String expressions
const A = 'abc';
document.writeln(`${A}d`);

// Logical expressions
const c = 5;
const d = 2;
const e = 'hello';
document.writeln(c === 5);
document.writeln(d === '2');
document.writeln(e === 'Hello');
