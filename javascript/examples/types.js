// string
const name = 'ram';
const otherName = 'hari';
const result = `The names are ${name} and ${otherName}`;

document.write(result);

// number
const a = 3;
const b = 3.433;
const c = 3e5; // 3 * 10^5

document.writeln(a + b + c);

// Infinity
const d = 3 / 0;

document.writeln(d);

// -Infinity
const e = -3 / 0;

document.writeln(e);
