// Set value of number qual to 5
const num = 5;

// Equal to
document.writeln(`Equal to: ${num}` === 5);
document.writeln('<br/>');

// Not Equal
document.writeln(`Not Equal: ${num}` !== 5);
document.writeln('<br/>');

// Strict Equal
document.writeln(`Strict Equal${num}` === 2);
document.writeln('<br/>');
